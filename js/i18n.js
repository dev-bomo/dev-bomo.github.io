function updateContent() {
    var translateTargets = document.querySelectorAll('[data-i18n]');
    translateTargets.forEach((item) => {
        var attr = item.getAttribute('data-i18n');
        item.textContent = i18next.t(attr);
    });

    var moreDetails = document.getElementById('moreDetails');
    moreDetails.href += window.a_lng;
}

function changeLng(lng) {
    i18next.changeLanguage(lng);
}

function stuff() {
    if (window.a_lng === 'ro') {
        i18next.changeLanguage('en');
        window.a_lng = 'en';
    } else if (window.a_lng === 'en') {
        i18next.changeLanguage('ro');
        window.a_lng = 'ro';
    }
}

i18next.on('languageChanged', () => {
    updateContent();
});